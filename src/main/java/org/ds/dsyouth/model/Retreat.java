package org.ds.dsyouth.model;

public class Retreat {

	private Integer id;
	private String year;
	private String season;
	private String retreatName;
	private String headerFirst;
	private String headerSecond;
	private Integer regUser;
	private String delYn;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSeason() {
		return season;
	}
	public void setSeason(String season) {
		this.season = season;
	}
	public String getRetreatName() {
		return retreatName;
	}
	public void setRetreatName(String retreatName) {
		this.retreatName = retreatName;
	}
	public String getHeaderFirst() {
		return headerFirst;
	}
	public void setHeaderFirst(String headerFirst) {
		this.headerFirst = headerFirst;
	}
	public String getHeaderSecond() {
		return headerSecond;
	}
	public void setHeaderSecond(String headerSecond) {
		this.headerSecond = headerSecond;
	}
	public Integer getRegUser() {
		return regUser;
	}
	public void setRegUser(Integer regUser) {
		this.regUser = regUser;
	}
	public String getDelYn() {
		return delYn;
	}
	public void setDelYn(String delYn) {
		this.delYn = delYn;
	}
	
}
